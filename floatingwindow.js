function escondeSeparador() {
  document.getElementById('separadorDeQuadros').style.display = "none";
  document.getElementById('separadorDeQuadrosFloatingBoxShadow').style.display = "none";
}
function mostraSeparador() {
  document.getElementById('separadorDeQuadros').style.display = "";
  document.getElementById('separadorDeQuadrosFloatingBoxShadow').style.display = "";
}
